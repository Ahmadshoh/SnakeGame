    def create_image(self):
        """ Создаёт изображение на основе списка """
        screen = pygame.Surface((841, 841), pygame.SRCALPHA, 32)
        x, y, = 1, 1
        for i in self.level:
            if i == 0:
                pygame.draw.rect(screen, pygame.Color('Brown'), pygame.Rect(x, y, 20, 20))
            x += 21
            if x == 841:
                y += 21
                x = 1
        pygame.image.save(screen, "images/gui.png")

    def draw_level(self, window):
        """ Отрисовывает уровень игры """
        window.blit(self.gui_image, (0, 0))

    def init_field(self):
        """ Заполняет списки с координатами """
        x, y = 1, 1
        for i in self.level:
            if i == 0:
                self.barrier.append([x, y])
            elif i == 1 and y != 22:
                self.field.append([x, y])
            x += 21
            if x == 841:
                y += 21
                x = 1

    def get_new_indicate(self):
        """ Заполняет список координатами индикатора """
        self.indicator.append([self.indicator[-1][0] + 21, 21])

